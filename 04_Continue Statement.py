# => Continue Statement 

# The continue statement is used to skip the remaining code inside a loop for the current iteration only. For instance, let's use continue instead of a break statement in the previous example.
# When the condition num == 5 becomes True , the continue statement gets executed.

# This code will print only the even numbers from 1 to 10.

# The continue statement can be used in any type of loop, for example, while loops and for loops.

# The continue statement is often used in conjunction with the break statement. 

# The break statement is used to exit a loop completely, while the continue statement is used to skip the current iteration of the loop and continue with the next one.

# The continue statement and the break statement are powerful tools that can be used to control the flow of your code