# Python uses a heap to manage memory. 
# A heap is a region of memory where objects are allocated. 
# The size of the memory allocation depends on the type of object being created.


# => Private heap: A portion of memory that is exclusive to the Python process. All Python objects and data structures are stored in the private heap.


# => Dynamic memory allocation: The interpreter allocates memory dynamically as needed.


# => Garbage collector: Automatically handles deallocation. Python also has an inbuilt garbage collector, which recycles all the unused memory and so that it can be made available to the heap space.